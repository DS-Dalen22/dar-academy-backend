package Work;

public class Arithmetic {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        int sum = a + b;
        int sum1 = a * b;
        int sum2 = a - b;
        int sum3 = a / b;
        int sum4 = a % b;

        System.out.println(sum);
        System.out.println(sum1);
        System.out.println(sum2);
        System.out.println(sum3);
        System.out.println(sum4);
    }
}
